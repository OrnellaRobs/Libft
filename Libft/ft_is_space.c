#include "libft.h"

int		ft_is_space(char *str)
{
	int i;

	i = 0;
	if (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' || str[i] == '\v' ||
		str[i] == '\f' || str[i] == '\f' || str[i] == '\r')
		return (1);
	return (0);
}
