#include "libft.h"

int		ft_is_space(char *str)
{
	if (*str == ' ' || *str == '\n' || *str == '\t' || *str == '\v' ||
		*str == '\f' || *str == '\f' || *str == '\r')
		return (1);
	return (0);
}
